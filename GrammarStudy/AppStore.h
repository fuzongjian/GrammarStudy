#ifndef AppStore_h
#define AppStore_h
/*
 
 一、项目上传到AppStore是这个项目的最后一个阶段，有两种方式上传到AppStore。
 
    首先---证书最基本，如果连证书都不懂，下面的步骤可以直接忽略。
 
    其次---项目archive成功之后，出现两个选择。
 
    第一个  a、export出ipa文件，export过程可能需要一定的时间，成功的标志就是桌面生成一个文件夹，里面有个ipa文件
           b、工具栏点击Xcode，Open Developer Tool -> Application Loader
           c、出现一个类似mac安装软件的弹框，点击“选取”，选择导出的ipa文件，然后下一步
           d、验证的过程可能有点儿漫长，最后出现弹框，里面有“生成的API分析文件太大，我们无法在交付前验证您的API使用信息，这只是通知信息”的字样，别担心，这是成功的标志。
           e、十分之后到开发中心项目提交审核就ok了
 
    第二个 点击validate进行验证，Validation Successful之后，然后Upload App Store成功之后，十分之后到开发中心项目提交审核
    
    两种方式第一种方式操作繁琐，但速度快，第二种操作简单但速度慢，各有利弊。
 
 
 
 
 
 
 
 
 
 
 */

#endif /* AppStore_h */
